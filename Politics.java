package com.technoelevate.collection.iterations;

public class Politics {

		private String name;
		private String state;
		
		public Politics(String name, String state) {
			super();
			this.name = name;
			this.state = state;
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}

		public String getState() {
			return state;
		}

		public void setWeapon(String state) {
			this.state = state;
		}
}
